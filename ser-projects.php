        // Collect form data
        let formData = {
          name: $('input[name="name_2[]"]').val(),
          email: $('input[name="email_2[]"]').val(),
          phone: $('input[name="phone_2[]"]').val(),
          message: $('textarea[name="message_2[]"]').val(),
        };

        // Validate email (additional JS validation if necessary)
        let emailPattern = /^[a-zA-Z0-9.!#$%&’*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.[a-zA-Z]+)+$/;
        if (!emailPattern.test(formData.email)) {
          alert("Please enter a valid email address");
          return;
        }

        // Send data via AJAX
        $.ajax({
          url: './send-email.php',
          type: 'POST',
          contentType: 'application/json',
          data: JSON.stringify(formData),
          success: function(response) {
            if (response === "true") {
              $(".form-alert")
                .removeClass("alert-danger")
                .addClass("alert-info")
                .text('Message Sent Successfully')
                .show();

              setTimeout(function() {
                $(".form-alert").hide();
                $("#cust_contact-form")[0].reset();
              }, 3000);
            } else {
              alert(response);
            }
          },
          error: function(xhr, status, error) {
            console.error("AJAX error: ", error);
            alert("An error occurred. Please try again.");
          }
        });
      });
